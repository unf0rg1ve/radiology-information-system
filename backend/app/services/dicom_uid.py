"""
Генерация валидных DICOM UID (Unique Identifiers) по ISO 9834-3 / RFC 4122.

DICOM UID состоит из числовых компонентов, разделённых точками.
Длина не более 64 символов. Компоненты — целые числа без ведущих нулей
(кроме самого компонента "0").

Используем OID-пространство 2.25, которое зарезервировано для UUID,
преобразованного в целое число. Это гарантирует глобальную уникальность
без необходимости регистрации собственного OID.
"""
import uuid


def generate_study_instance_uid() -> str:
    """Сгенерировать валидный DICOM StudyInstanceUID."""
    uid_int = uuid.uuid4().int
    return f"2.25.{uid_int}"
